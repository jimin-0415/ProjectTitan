#include "pch.h"
#include "ReadLockGuard.h"
#include "RWSpinLock.h"


/**********************************************************************************************************************
* @brief 생성자
**********************************************************************************************************************/
ReadLockGuard::ReadLockGuard( RWSpinLock& lock )
:
_lock( lock )
{
	_lock.ReadLock();
}

/**********************************************************************************************************************
* @brief 소멸자
**********************************************************************************************************************/
ReadLockGuard::~ReadLockGuard()
{
	_lock.ReadUnlock();
}
