/**********************************************************************************************************************
* @brief 강제 크래시 매크로
**********************************************************************************************************************/
#define CRASH( cause )                                                                                                \
{                                                                                                                     \
	ExUInt32* crash = nullptr;                                                                                        \
	__analysis_assume( crash != nullptr );                                                                            \
    *crash = 0xDEADBEEF;                                                                                              \
}

/**********************************************************************************************************************
* @brief 강제 크래시 매크로
**********************************************************************************************************************/
#define ASSERT_CRASH( expr )                                                                                          \
{                                                                                                                     \
	if (!(expr))                                                                                                      \
	{                                                                                                                 \
		CRASH("ASSERT_CRASH!!!");                                                                                     \
		__analysis_assume(expr);                                                                                      \
	}                                                                                                                 \
}